package org.petrzhang.sora.graphics.utils;

public class WindowAttrib {

    private int key;
    private int value;

    public WindowAttrib(int key, int value){
        this.key = key;
        this.value = value;
    }

    public int getKey() {
        return key;
    }

    public int getValue() {
        return value;
    }
}
